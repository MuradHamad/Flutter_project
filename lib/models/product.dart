class Product {
  final int id;
  final String name;
  final String catagory;
  final String image;
  final double price;
  final String description;
  int quantity;

  Product({
    required this.id,
    required this.name,
    required this.catagory,
    required this.image,
    required this.price,
    required this.description,
    required this.quantity,
  });
}
